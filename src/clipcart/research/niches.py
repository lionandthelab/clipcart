def product_type_ok(name: str, keyword: str) -> bool:
    """상품명이 니치의 상품 유형 토큰과 일치하는지(대본/상품 불일치 방지)."""
    tokens = PRODUCT_MATCH.get(keyword)
    if not tokens:
        return True
    n = (name or "").replace(" ", "").lower()
    return any(tok.replace(" ", "").lower() in n for tok in tokens)


